    #[test]
    fn test_multiple_passkey_credentials() {
        let env = Env::default();
        let admin = Address::generate(&env);
        let user = Address::generate(&env);

        // Initialize contract
        crate::SoroSusuTrait::init(env.clone(), admin.clone());

        let origin = Symbol::new(&env, "https://sorosusu.app");

        // Register first passkey
        env.mock_all_auths();
        let public_key1 = BytesN::from_array(&env, &[0x02; 33]);
        let credential_id1 = Bytes::from_slice(&env, &[0x01, 0x02, 0x03, 0x04]);
        
        PasskeyTestContract::test_register_passkey(
            env.clone(),
            user.clone(),
            public_key1,
            credential_id1.clone(),
            origin,
        ).unwrap();

        // Register second passkey
        let public_key2 = BytesN::from_array(&env, &[0x03; 33]);
        let credential_id2 = Bytes::from_slice(&env, &[0x05, 0x06, 0x07, 0x08]);
        
        PasskeyTestContract::test_register_passkey(
            env.clone(),
            user.clone(),
            public_key2,
            credential_id2.clone(),
            origin,
        ).unwrap();

        // Verify both credentials are stored
        let profile = PasskeyTestContract::test_get_auth_profile(env.clone(), user.clone())
            .unwrap();
        assert_eq!(profile.passkey_credentials.len(), 2);

        // Try to register a third passkey (should succeed)
        let public_key3 = BytesN::from_array(&env, &[0x04; 33]);
        let credential_id3 = Bytes::from_slice(&env, &[0x09, 0x0A, 0x0B, 0x0C]);
        
        PasskeyTestContract::test_register_passkey(
            env.clone(),
            user.clone(),
            public_key3,
            credential_id3.clone(),
            origin,
        ).unwrap();

        // Verify all three credentials are stored
        let profile = PasskeyTestContract::test_get_auth_profile(env.clone(), user.clone())
            .unwrap();
        assert_eq!(profile.passkey_credentials.len(), 3);

        // Try to register a fourth passkey (should fail - max limit reached)
        let public_key4 = BytesN::from_array(&env, &[0x05; 33]);
        let credential_id4 = Bytes::from_slice(&env, &[0x0D, 0x0E, 0x0F, 0x10]);
        
        let result = PasskeyTestContract::test_register_passkey(
            env.clone(),
            user.clone(),
            public_key4,
            credential_id4,
            origin,
        );

        assert!(result.is_err());
        assert_eq!(result.unwrap_err(), crate::passkey_auth::PasskeyError::TooManyCredentials as u32);
    }

