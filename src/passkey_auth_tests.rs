    #[test]
    fn test_auth_method_preference() {
        let env = Env::default();
        let admin = Address::generate(&env);
        let user = Address::generate(&env);

        // Initialize contract
        crate::SoroSusuTrait::init(env.clone(), admin.clone());

        // Initially should default to Ed25519
        let profile = PasskeyTestContract::test_get_auth_profile(env.clone(), user.clone());
        assert!(profile.is_err(), "No profile should exist initially");

        // Register a passkey
        let public_key = BytesN::from_array(&env, &[0x02; 33]);
        let credential_id = Bytes::from_slice(&env, &[0x01, 0x02, 0x03, 0x04]);
        let origin = Symbol::new(&env, "https://sorosusu.app");

        env.mock_all_auths();
        PasskeyTestContract::test_register_passkey(
            env.clone(),
            user.clone(),
            public_key,
            credential_id,
            origin,
        ).unwrap();

        // Should now default to Secp256r1
        let profile = PasskeyTestContract::test_get_auth_profile(env.clone(), user.clone())
            .unwrap();
        assert_eq!(profile.preferred_method, crate::passkey_auth::AuthMethod::Secp256r1);

        // Change preference back to Ed25519
        env.mock_all_auths();
        PasskeyTestContract::test_set_preferred_auth_method(
            env.clone(),
            user.clone(),
            crate::passkey_auth::AuthMethod::Ed25519,
        ).unwrap();

        // Verify preference changed
        let profile = PasskeyTestContract::test_get_auth_profile(env.clone(), user.clone())
            .unwrap();
        assert_eq!(profile.preferred_method, crate::passkey_auth::AuthMethod::Ed25519);
    }

